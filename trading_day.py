"""
A股交易日判断（简化版）。

规则：交易日 = 周一~周五 且 不在当年休市集合中。
休市集合含：国家法定节假日 + 交易所公告的调休休市日。

注意：每年具体休市安排以「上海证券交易所 / 深圳证券交易所」当年官方公告为准。
下方 HOLIDAYS_BY_YEAR 为基于国务院放假安排的近似表，请在上一年末核对交易所公告后更新。
"""
from datetime import date, timedelta

# 近似休市日（格式 YYYY-MM-DD）。每年需按交易所公告核对更新。
# 下面 2026 年数据为基于公开放假安排的近似，并非交易所最终公告，使用前请核对。
HOLIDAYS_BY_YEAR = {
    2026: {
        "2026-01-01",  # 元旦
        # 春节（近似，以公告为准）
        "2026-02-16", "2026-02-17", "2026-02-18", "2026-02-19", "2026-02-20",
        "2026-02-23", "2026-02-24",
        # 清明（近似）
        "2026-04-04", "2026-04-05", "2026-04-06",
        # 劳动节（近似）
        "2026-05-01", "2026-05-02", "2026-05-03", "2026-05-04", "2026-05-05",
        # 端午（近似）
        "2026-06-19", "2026-06-20", "2026-06-21",
        # 中秋（近似）
        "2026-09-25", "2026-09-26", "2026-09-27",
        # 国庆（近似）
        "2026-10-01", "2026-10-02", "2026-10-03", "2026-10-04",
        "2026-10-05", "2026-10-06", "2026-10-07",
    },
}


def is_weekend(d: date) -> bool:
    """周六(5)/周日(6) 返回 True。"""
    return d.weekday() >= 5


def is_trading_day(d: date = None) -> bool:
    """判断给定日期是否为 A股交易日。默认今天。"""
    d = d or date.today()
    if is_weekend(d):
        return False
    holidays = HOLIDAYS_BY_YEAR.get(d.year, set())
    if d.strftime("%Y-%m-%d") in holidays:
        return False
    return True


def next_trading_day(d: date = None) -> date:
    """返回 d 之后的第一个交易日（含 d 本身若为交易日）。"""
    d = d or date.today()
    while not is_trading_day(d):
        d += timedelta(days=1)
    return d
